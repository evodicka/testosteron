package org.kuhlins.app.testosteron.model;

public class SystemInfo {
	
	private String nets;
	
	private String disks;
	
	private String mem;

	public String getNets() {
		return nets;
	}

	public void setNets(String nets) {
		this.nets = nets;
	}

	public String getDisks() {
		return disks;
	}

	public void setDisks(String disks) {
		this.disks = disks;
	}

	public String getMem() {
		return mem;
	}

	public void setMem(String mem) {
		this.mem = mem;
	}
	
	

	
}
